//metodo responsavel pela consulta de usuarios

    private void consultar() {
        String sql = "select * from tbusuarios where iduser = ?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuID.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                txtUsuNome.setText(rs.getString(2));
                txtUsuLogin.setText(rs.getString(3));
                txtUsuSenha.setText(rs.getString(4));
                cboUsuPerfil.setSelectedItem(rs.getString(5));

            } else {
                JOptionPane.showMessageDialog(null, "Usuario não encontrado");
                txtUsuLogin.setText(null);
                txtUsuNome.setText(null);
                txtUsuSenha.setText(null);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
//metodo responsavel pela adição de usuarios

    private void adicionar() {
        String sql = "insert into tbusuarios(iduser,usuario,login,senha,perfil) values (?,?,?,?,?);";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuID.getText());
            pst.setString(2, txtUsuNome.getText());
            pst.setString(3, txtUsuLogin.getText());
            pst.setString(4, txtUsuSenha.getText());
            pst.setString(5, cboUsuPerfil.getSelectedItem().toString());
            //validação dos campos 
            if ((txtUsuID.getText().isEmpty()) || (txtUsuLogin.getText().isEmpty()) || (txtUsuNome.getText().isEmpty() || (txtUsuSenha.getText().isEmpty()))) {
                JOptionPane.showMessageDialog(null, "Preencha os campos obrigatórios");
            } else {

                int adicionado = pst.executeUpdate();
                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "Usuário cadastrado com sucesso");
                    txtUsuID.setText(null);
                    txtUsuLogin.setText(null);
                    txtUsuNome.setText(null);
                    txtUsuSenha.setText(null);
                    cboUsuPerfil.setSelectedItem(null);
                } else {
                    JOptionPane.showMessageDialog(null, "Não foi possível cadastrar usuário");
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
//metodo responsavel pela alteração de usuarios

