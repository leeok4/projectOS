    public void logar() {
        // SQL de consulta no banco
        String sql = "select * from tbusuarios where login=? and senha=?";
        try {
            //consulta de usuario e senha para login
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuario.getText());
            pst.setString(2, txtPassword.getText());
            rs = pst.executeQuery();

            //caso exista usuario e senha
            if (rs.next()) {
                // verificando permiss√£o do usuario 
                String perfil = rs.getString(5);

                //System.out.println(perfil);
                if (perfil.equals("admin")) {

                    TelaPrincipal tp = new TelaPrincipal();
                    tp.setVisible(true);
                    tp.menRelServ.setEnabled(true);
                    tp.menCadUsuario.setEnabled(true);
                    tp.lblUsuario.setText(rs.getString(2));
                    tp.lblUsuario.setForeground(Color.red);
                    this.dispose();
                } else {
                    TelaPrincipal tp = new TelaPrincipal();
                    tp.setVisible(true);
                    tp.lblUsuario.setText(rs.getString(2));
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Usuario ou senha incorretos");
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }

